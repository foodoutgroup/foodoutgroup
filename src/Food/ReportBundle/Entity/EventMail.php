<?php

namespace Food\ReportBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * EventMail
 *
 * @ORM\Table(name="event_mail")
 * @ORM\Entity(repositoryClass="Food\ReportBundle\Entity\EventMailRepository")
 */
class EventMail
{
    /**
     * @var integer
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     *
     * @ORM\Column(name="email", type="string", length=255)
     */
    private $email;

    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set email
     *
     * @param string $email
     * @return EventMail
     */
    public function setEmail($email)
    {
        $this->email = $email;

        return $this;
    }

    /**
     * Get email
     *
     * @return string 
     */
    public function getEmail()
    {
        return $this->email;
    }

}
